const number1 = prompt( "Enter number1");
const number2 = prompt( "Enter number2");

const sum = Number(number1) + Number(number2);
console.log(`Result: ${number1} + ${number2} = ${sum}`);

const subtraction = number1 - number2;
console.log(`Result: ${number1} - ${number2} = ${subtraction}`);

const dividing = number1 / number2;
console.log(`Result: ${number1} / ${number2} = ${dividing}`);

const multiplication = number1 * number2;
console.log(`Result: ${number1} * ${number2} = ${multiplication}`);